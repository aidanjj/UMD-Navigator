package com.example.testline;

public class PointDP {
    private float x;
    private float y;
    public PointDP(){
        this.x = 0;
        this.y = 0;
    }
    public PointDP(float x, float y){
        this.x = x;
        this.y = y;
    }
    public float getX(){
        return x;
    }
    public float getY(){
        return y;
    }
}
